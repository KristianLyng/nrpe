#ifndef HEADER_DH_H
#include <openssl/dh.h>
#endif
DH *get_dh512()
	{
	static unsigned char dh512_p[]={
		0xAC,0xDA,0x27,0xF4,0x96,0xEC,0x5D,0x76,0x1B,0xC9,0x67,0x6C,
		0xD7,0x0B,0xDC,0x31,0x2E,0xCC,0x95,0x6F,0x55,0xF5,0x4F,0xD0,
		0x45,0x4B,0xC1,0x49,0x5F,0x1C,0x00,0xE3,0xEE,0xED,0xD7,0x6F,
		0x56,0x6D,0xF8,0xFD,0x33,0x23,0x06,0x53,0x2D,0x7B,0x47,0xB2,
		0xE4,0x81,0x02,0xDB,0x19,0x69,0x0B,0x86,0xD9,0xDA,0x2B,0x37,
		0x44,0xE4,0x0C,0x03,
		};
	static unsigned char dh512_g[]={
		0x02,
		};
	DH *dh;

	if ((dh=DH_new()) == NULL) return(NULL);
	dh->p=BN_bin2bn(dh512_p,sizeof(dh512_p),NULL);
	dh->g=BN_bin2bn(dh512_g,sizeof(dh512_g),NULL);
	if ((dh->p == NULL) || (dh->g == NULL))
		{ DH_free(dh); return(NULL); }
	return(dh);
	}
